#include <unistd.h>

void	ft_print_testi(void)
{
	int	num;
	num = '9';
	while (num >= '0')
	{
		write (1, &num, 1);
		num --;
	}
}
int	main(void)
{
	ft_print_testi();
	return (0);
}
